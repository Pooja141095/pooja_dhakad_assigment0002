

// Simple Interest

let principal=Number(prompt("Enter Principal Amount"));
let rate=Number(prompt("Enter Rate of Interest 1 to 10%"));
let time=Number(prompt("Time Period 1-12"));
alert((principal*rate*time)/100);

// Area of circle 

let radius=Number(prompt("Enter Radius you will get the area"));
alert((Math.PI*radius*radius));

// Volume of cube

let edge=Number(prompt("Enter Edge you will get the area of cube"));
alert((6*edge*edge));

// Surface Area of Cylinder

let Radius=Number(prompt("Enter the Radius "));
let height=Number(prompt("Enter the Height "));
alert((2*Math.PI*Radius*height));
